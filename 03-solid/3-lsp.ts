// 3. Liskov Substitution Principle (LSP):

// Subtypes must be substitutable for their base types without altering the correctness of the program.
// This principle ensures that objects of derived classes can replace objects of the base class without causing unexpected behavior.
// It promotes polymorphism and helps maintain consistency in object-oriented hierarchies.

// The idea here is, if it talk like a duck, quack like a duck, fly like a duck it must be a duck.
// The principle encourages us to use composition over inheritance.
